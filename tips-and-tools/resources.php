                    <h3>Alumni Card Policy</h3>
                    <p>John Marshall alumni have the benefit of library privileges at the Louis L. Biro Law Library after obtaining a John Marshall alumni ID card. The card can be secured at the Security Office on the 2nd floor of the State Street building. The Security Office is normally open Monday-Friday, 11 a.m.-9 p.m. (Please check availability by calling 312.427.2737 ext. 309.) To have an ID card issued, applicants must complete an application form and have a photo taken. There is no initial charge for the ID card; however, a $10 fee will be charged for replacement of lost cards. In addition to providing access to the library, the ID card is used as a copy/printing card. Printing/copying credit can be purchased at the 2nd floor Security Office. A valid ID is needed to enter the library and to check out circulating materials.</p>
					<h3>LexisNexis &amp; Westlaw Access</h3>
					<p>For the convenience of our alumni, the Louis L. Biro Library subscribes to versions of Westlaw and Lexis that are available to alumni for use on campus at one of four desktop computers designated for alumni and CBA members. Please note that the Westlaw subscription allows two users at a time, so on some occasions there may be a wait until current users log off. The cost of printing is $0.15 per page. Printing credit can be purchased at the 2nd floor Security Office.</p>
                    <h3>Access to Other Library Databases</h3>
                    <p>In addition to Westlaw and Lexis, alumni have access to other databases (e.g. Heinonline for searching journals) on the desktop computers designated for alumni and CBA members. Please note some databases are only available for current John Marshall students and staff. In all cases, databases are available only in the library. Please inquire at the reference desk if a particular database is available for use. </p>
					<h3>Free WiFi</h3>
					<p>The library provides alumni limited wireless access while in the library. Please ask at the reference or circulation desk how to access the wifi network. At this time, printing is not available from the guest network.</p>
