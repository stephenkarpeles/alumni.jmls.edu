					<h1 class="page-header">Cy Pres Award</h1>					
					<p class="featured-text">Cy Pres awards are residual funds in class action cases (and occasionally in other types of proceedings like bankruptcy and probate matters) that, for any number of reasons, are unclaimed or cannot be distributed to the class members or beneficiaries who were the intended recipients.</p>
					<p>Under the Cy Pres doctrine and Illinois law, courts can distribute these residual funds to appropriate charitable causes, and The John Marshall Law School is an appropriate beneficiary.</p>
					<p>Directing Cy Pres funds towards The John Marshall Law School advances our school&rsquo;s 110-year mission of providing access and opportunity to the legal profession. Cy Pres funds can also provide community support when such awards are directed to programs like our <a href="http://www.jmls.edu/veterans/" target="_blank"><strong>Veteran&rsquo;s Legal Support Center &amp; Clinic</strong></a> and <a href="http://www.jmls.edu/fairhousing/" target="_blank"><strong>Fair Housing Legal Support Center &amp; Clinic</strong></a>.</p>

					<h3 class="mar-bot-medium">Cy Pres and the State of Illinois</h3>

					<p>Section <a href="http://www.jmls.edu/foundation/pdf/civil-procedure.pdf" target="_blank"><strong>35 ILCS 5/2-807</strong></a> of the Illinois Code authorizes the distribution of up to 50% of unclaimed funds from court proceedings to support organizations that serve the public good. Examples of organizations receiving Cy Pres support include: legal assistance foundations; educational institutions with clinical programs; and neighborhood-based outreach groups. Awards have reached $5 million in recent years, as noted by the Circuit Court of Cook County and Chief Judge Tim Evans (JMLS &lsquo;68). See: <a href="http://www.jmls.edu/foundation/pdf/Circuit-Court-approves-distribution-of-close-to-5-million-in-cy-pres-awards.pdf" target="_blank"><strong>&ldquo;Circuit Court approves distribution of close to $5 million in Cy Pres awards.&rdquo;</strong></a></p>
					<p>Cy Pres awards give organizations like The John Marshall Law School unique opportunities to help promote access and opportunity in the legal profession and provide community support.</p>
